// Calling Package
package main

import "fmt"

// Function
func main() {

	//Printing : String
	fmt.Println("Hello" + "World")

	//Printing : String + Number
	fmt.Println(" 9 X 10 = ", 9*10)

	//Printing : String + Float Number
	fmt.Println("180.18/2.0", 180.18/2.0)

	//Printing : && => True False
	fmt.Println(true && false)

	//Printing : || => True False
	fmt.Println(true || false)

	//Print NOT Boolean
	fmt.Println(!true)
}
